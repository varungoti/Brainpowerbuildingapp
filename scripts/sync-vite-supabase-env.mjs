/**
 * Writes `.env.local` with VITE_SUPABASE_* from the linked project (anon key only).
 * Requires: `.env.supabase` with SUPABASE_ACCESS_TOKEN, and `supabase link` (project-ref in supabase/.temp).
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const envSupabase = resolve(root, ".env.supabase");
const projectRefFile = resolve(root, "supabase", ".temp", "project-ref");

if (!existsSync(envSupabase)) {
  console.error("Missing .env.supabase");
  process.exit(1);
}
if (!existsSync(projectRefFile)) {
  console.error("Missing supabase/.temp/project-ref — run: node scripts/run-supabase-cli.mjs link --project-ref YOUR_REF --yes");
  process.exit(1);
}

const projectRef = readFileSync(projectRefFile, "utf8").trim();
if (!projectRef) {
  console.error("Empty project ref");
  process.exit(1);
}

for (const line of readFileSync(envSupabase, "utf8").split("\n")) {
  const t = line.trim();
  if (!t || t.startsWith("#")) continue;
  const eq = t.indexOf("=");
  if (eq <= 0) continue;
  process.env[t.slice(0, eq).trim()] = t.slice(eq + 1).trim();
}

const r = spawnSync(
  "npx",
  ["supabase", "projects", "api-keys", "--project-ref", projectRef, "-o", "json"],
  { cwd: root, encoding: "utf8", env: process.env, shell: true },
);
if (r.status !== 0) {
  console.error(r.stderr || r.stdout || "api-keys failed");
  process.exit(r.status ?? 1);
}

const keys = JSON.parse(r.stdout);
const anon = keys.find((k) => k.id === "anon" && k.name === "anon");
if (!anon?.api_key) {
  console.error("Could not find legacy anon key in API response.");
  process.exit(1);
}

const url = `https://${projectRef}.supabase.co`;
const body =
  `# Generated by scripts/sync-vite-supabase-env.mjs — gitignored\n` +
  `VITE_SUPABASE_PROJECT_ID=${url}\n` +
  `VITE_SUPABASE_ANON_KEY=${anon.api_key}\n`;

writeFileSync(resolve(root, ".env.local"), body, "utf8");
console.log("Updated .env.local with VITE_SUPABASE_PROJECT_ID and VITE_SUPABASE_ANON_KEY.");
